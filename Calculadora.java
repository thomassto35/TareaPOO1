package org.example.CalculadoraBasica;

public class Calculadora {
    public static float sumar(float nro1, float nro2){
        return nro1+nro2;
    }
    public static float restar(float nro1, float nro2){
        return nro1-nro2;
    }
    public static float multiplicar(float nro1, float nro2){
        return nro1*nro2;
    }
    public static float dividir(float nro1, float nro2){
        return nro1/nro2;
    }
}
